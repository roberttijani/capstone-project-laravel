    {
        $request->validate([
            'excel' => 'required|file'
        ]);
        $data = Excel::toCollection(new UsulanImport, $request->file('excel')->store('excel'));
        // $request->file('excel')->store('excel');

        return view('excel',[
            'data'=> $data
        ])->with('success','Excel Uploaded');
    }

